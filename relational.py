num1=int(input("Enter First Number :"))#100
num2=int(input("Enter Second Number :"))#100
print("{} > {} = {}".format(num1,num2,num1>num2))#False
print("{} < {} = {}".format(num1,num2,num1<num2))#False
print("{} >= {} = {}".format(num1,num2,num1>=num2))#True
print("{} <= {} = {}".format(num1,num2,num1<=num2))#True
print("{} == {} = {}".format(num1,num2,num1==num2))#True
print("{} != {} = {}".format(num1,num2,num1!=num2))#False
